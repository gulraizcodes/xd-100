/*=============== SHOW MENU ===============*/
const navMenu = document.getElementById('n-menu'),
      navToggle = document.getElementById('n-toggle'),
      navClose = document.getElementById('n-close')

/*===== MENU SHOW =====*/
/* Validate if constant exists */
if(navToggle){
    navToggle.addEventListener('click', () =>{
        navMenu.classList.add('show-menu')
    })
}

/*===== MENU HIDDEN =====*/
/* Validate if constant exists */
if(navClose){
    navClose.addEventListener('click', () =>{
        navMenu.classList.remove('show-menu')
    })
}